    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request,
            ['email'=> 'required','phone'=> 'required','fullname'=> 'required'],
            ['email.required'=>'Không được để trống email !','phone.required'=>'Không được để trống số điện thoại !','fullname.required'=>'Không được để trống tên khách hàng !']
        );

        // dd($request);
        User::find($id)->update($request->all());
        return redirect()->route('list-user');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {   
        Order::where('id_cus',$id)->delete();
        User::destroy($id);
        return redirect()->route('list-user');
    }
